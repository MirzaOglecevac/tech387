/**
 * Function for translating page to english
 */
function changeToEnglish(){

    var text;

    if(sessionStorage.getItem('page') === 'aboutus'){
        text = englishAboutUs;
    }else if(sessionStorage.getItem('page') === 'team'){
        text = englishTeam;
    }else if(sessionStorage.getItem('page') === 'contact'){
        text = englishContact;
    }else {
        text = englishMain;
    }

    var data = document.getElementsByClassName('data');

    for(var i = 0; i < data.length; i++){
        if(data[i].hasAttribute('placeholder')){
            data[i].placeholder = text[i];
        }else {
            data[i].textContent = text[i];
        }
    }
}


